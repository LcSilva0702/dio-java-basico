public class ex1METODOS{
    

    public static void soma(double numUm, double numDois){
        double resultado = numUm + numDois;

        System.out.println("A soma de "+ numUm + " e " + numDois + " e igual a: " + resultado);
    }
    
    public static void subtracao(double numUm, double numDois){
        double resultado = numUm - numDois;

        System.out.println("A subtracao de "+ numUm + " e " + numDois + " e igual a: " + resultado);
    }

    public static void divisao(double numUm, double numDois){
        double resultado = numUm / numDois;

        System.out.println(" A divisao de "+ numUm + " e " + numDois + " e igual a: " + resultado);
    }

    public static void multiplicacao(double numUm, double numDois){
        double resultado = numUm * numDois;

        System.out.println("A multiplicacao de "+ numUm + " e " + numDois + " e igual a: " + resultado);
    }
}